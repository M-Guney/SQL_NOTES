--Bir yarýþtaymýþ gibi iki tane 1. olabiliyor fakat 2 ATLANMAZ 3'e geçer.
Select RANK() OVER
		(ORDER BY UNITPRICE DESC) AS RANK_ ,
		ROW_NUMBER() OVER
		(ORDER BY UNITPRICE DESC) AS ROWNUMBER ,
		DENSE_RANK() OVER
		(ORDER BY UNITPRICE DESC) AS DENSE_RANK_,*
FROM ITEMS


